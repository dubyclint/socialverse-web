<template>
  <div class="bg-slate-800 rounded-lg shadow-xl p-8 border border-slate-700">
    <!-- Error Message -->
    <div v-if="error" class="mb-4 p-4 bg-red-900/20 border border-red-500/50 rounded-lg">
      <p class="text-red-400 text-sm">{{ error }}</p>
    </div>

    <!-- Success Message -->
    <div v-if="success" class="mb-4 p-4 bg-green-900/20 border border-green-500/50 rounded-lg">
      <p class="text-green-400 text-sm">{{ success }}</p>
    </div>

    <form @submit.prevent="handleSubmit" class="space-y-4">
      <!-- Email Field -->
      <div>
        <label for="email" class="block text-sm font-medium text-slate-300 mb-2">
          Email Address
        </label>
        <input
          id="email"
          v-model="formData.email"
          type="email"
          required
          placeholder="your@email.com"
          :disabled="loading"
          class="w-full px-4 py-2 bg-slate-700 border border-slate-600 rounded-lg text-white placeholder-slate-500 focus:outline-none focus:border-blue-500 focus:ring-1 focus:ring-blue-500 disabled:opacity-50"
        />
        <p v-if="errors.email" class="mt-1 text-xs text-red-400">{{ errors.email }}</p>
      </div>

      <!-- Username Field - NO VALIDATION -->
      <div>
        <label for="username" class="block text-sm font-medium text-slate-300 mb-2">
          Username
        </label>
        <input
          id="username"
          v-model="formData.username"
          type="text"
          required
          placeholder="username"
          :disabled="loading"
          class="w-full px-4 py-2 bg-slate-700 border border-slate-600 rounded-lg text-white placeholder-slate-500 focus:outline-none focus:border-blue-500 focus:ring-1 focus:ring-blue-500 disabled:opacity-50"
        />
        <p class="mt-1 text-xs text-slate-400">
          3-30 characters, letters, numbers, underscores, hyphens only
        </p>
        <p v-if="errors.username" class="mt-1 text-xs text-red-400">{{ errors.username }}</p>
      </div>

      <!-- Password Field -->
      <div>
        <label for="password" class="block text-sm font-medium text-slate-300 mb-2">
          Password
        </label>
        <input
          id="password"
          v-model="formData.password"
          type="password"
          required
          placeholder="Create a strong password"
          :disabled="loading"
          class="w-full px-4 py-2 bg-slate-700 border border-slate-600 rounded-lg text-white placeholder-slate-500 focus:outline-none focus:border-blue-500 focus:ring-1 focus:ring-blue-500 disabled:opacity-50"
        />
        <p class="mt-1 text-xs text-slate-400">
          Min 8 chars, 1 uppercase, 1 number, 1 special character (@$!%*?&)
        </p>
        <p v-if="errors.password" class="mt-1 text-xs text-red-400">{{ errors.password }}</p>
      </div>

      <!-- Confirm Password Field -->
      <div>
        <label for="confirmPassword" class="block text-sm font-medium text-slate-300 mb-2">
          Confirm Password
        </label>
        <input
          id="confirmPassword"
          v-model="formData.confirmPassword"
          type="password"
          required
          placeholder="Confirm your password"
          :disabled="loading"
          class="w-full px-4 py-2 bg-slate-700 border border-slate-600 rounded-lg text-white placeholder-slate-500 focus:outline-none focus:border-blue-500 focus:ring-1 focus:ring-blue-500 disabled:opacity-50"
        />
        <p v-if="errors.confirmPassword" class="mt-1 text-xs text-red-400">
          {{ errors.confirmPassword }}
        </p>
      </div>

      <!-- Terms & Conditions -->
      <div class="flex items-start">
        <input
          id="terms"
          v-model="formData.agreeToTerms"
          type="checkbox"
          required
          :disabled="loading"
          class="mt-1 rounded border-slate-600 text-blue-500 focus:ring-blue-500"
        />
        <label for="terms" class="ml-2 text-sm text-slate-400">
          I agree to the
          <NuxtLink to="/terms" class="text-blue-500 hover:text-blue-400">
            Terms of Service
          </NuxtLink>
          and
          <NuxtLink to="/privacy" class="text-blue-500 hover:text-blue-400">
            Privacy Policy
          </NuxtLink>
        </label>
      </div>

      <!-- Submit Button -->
      <button
        type="submit"
        :disabled="loading || !formData.agreeToTerms"
        class="w-full py-2 px-4 bg-blue-600 hover:bg-blue-700 disabled:bg-slate-600 disabled:opacity-50 text-white font-semibold rounded-lg transition-colors"
      >
        <span v-if="loading" class="flex items-center justify-center">
          <svg class="animate-spin -ml-1 mr-3 h-5 w-5 text-white" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
            <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4"></circle>
            <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
          </svg>
          Creating account...
        </span>
        <span v-else>Create Account</span>
      </button>
    </form>
  </div>
</template>

<script setup lang="ts">
import { ref, reactive } from 'vue'

const emit = defineEmits<{
  success: [data: any]
}>()

const { signup } = useAuth()

const loading = ref(false)
const error = ref('')
const success = ref('')

const formData = reactive({
  email: '',
  username: '',
  password: '',
  confirmPassword: '',
  agreeToTerms: false
})

const errors = reactive({
  email: '',
  username: '',
  password: '',
  confirmPassword: ''
})

const validateForm = () => {
  errors.email = ''
  errors.username = ''
  errors.password = ''
  errors.confirmPassword = ''

  // Email validation
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
  if (!emailRegex.test(formData.email)) {
    errors.email = 'Please enter a valid email address'
  }

  // Username - BASIC FORMAT ONLY (no availability check)
  if (formData.username.length < 3 || formData.username.length > 30) {
    errors.username = 'Username must be 3-30 characters'
  }
  if (!/^[a-zA-Z0-9_-]+$/.test(formData.username)) {
    errors.username = 'Username can only contain letters, numbers, underscores, and hyphens'
  }

  // Password validation
  const passwordRegex = /^(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/
  if (!passwordRegex.test(formData.password)) {
    errors.password = 'Password must be at least 8 characters with 1 uppercase, 1 number, and 1 special character'
  }

  // Confirm password
  if (formData.password !== formData.confirmPassword) {
    errors.confirmPassword = 'Passwords do not match'
  }

  return !errors.email && !errors.username && !errors.password && !errors.confirmPassword
}

const handleSubmit = async () => {
  error.value = ''
  success.value = ''

  if (!validateForm()) {
    error.value = 'Please fix the errors above'
    return
  }

  loading.value = true

  try {
    const result = await signup(
      formData.email,
      formData.password,
      formData.username
    )

    if (result.success) {
      success.value = result.message
      setTimeout(() => {
        emit('success', { email: formData.email })
      }, 1000)
    } else {
      error.value = result.error || 'Signup failed'
    }
  } catch (err: any) {
    error.value = err.message || 'An error occurred'
  } finally {
    loading.value = false
  }
}
</script>
